    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        Boolean result = Admin.AddCar(Global.RemoveWhitespaces(CarPlate.getText().toUpperCase()), Global.RemoveWhitespaces(CarType.getText()), Global.RemoveWhitespaces(Price.getText()), Global.RemoveWhitespaces(Color.getText()));
        if (result == true) {
            this.setVisible(false);
            this.dispose();
            AdminNewCar page = new AdminNewCar();
            page.setVisible(true);
        } else {
            CarPlate.setText("");
            CarType.setText("");
            Price.setText("");
            Color.setText("");
        }
    }//GEN-LAST:event_AddBtnActionPerformed

